    return render(request, 'core/random_food.html', {
        'food': food,
        'form': form,
        'avg_rating': avg_rating if food else None,  # ‡∏™‡πà‡∏á‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡πÄ‡∏â‡∏•‡∏µ‡πà‡∏¢‡πÑ‡∏õ‡∏ó‡∏µ‡πà template
    })
