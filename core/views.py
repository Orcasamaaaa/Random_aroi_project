@login_required
def recommend_food(request):
    user = request.user
    liked_foods = LikeDislikeFood.objects.filter(user=user)  # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏≠‡∏≤‡∏´‡∏≤‡∏£‡∏ó‡∏µ‡πà‡πÄ‡∏Ñ‡∏¢‡∏ä‡∏≠‡∏ö/‡πÑ‡∏°‡πà‡∏ä‡∏≠‡∏ö

    if not liked_foods.exists():
        messages.warning(request, "‡∏Ñ‡∏∏‡∏ì‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Å‡∏≤‡∏£‡∏ä‡∏≠‡∏ö/‡πÑ‡∏°‡πà‡∏ä‡∏≠‡∏ö‡∏≠‡∏≤‡∏´‡∏≤‡∏£ ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÉ‡∏´‡πâ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏≠‡∏≤‡∏´‡∏≤‡∏£‡∏Å‡πà‡∏≠‡∏ô!")
        return render(request, 'core/recommend_food.html', {'recommendations': []})

    # üîπ 1. ‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• Feature ‡∏Ç‡∏≠‡∏á‡∏≠‡∏≤‡∏´‡∏≤‡∏£
    foods = Food.objects.all()
    food_data = []
    food_labels = []
    food_ids = []
    category_list = set()  # ‡πÄ‡∏Å‡πá‡∏ö‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö One-Hot Encoding

    for log in liked_foods:
        food = log.food
        category_names = [c.name for c in food.category.all()]
        category_list.update(category_names)  # ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏ä‡∏∑‡πà‡∏≠‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà‡πÄ‡∏Ç‡πâ‡∏≤‡πÄ‡∏ã‡πá‡∏ï
        food_data.append([food.price, *category_names])
        food_labels.append(1 if log.liked else 0)  # 1 = ‡∏ä‡∏≠‡∏ö, 0 = ‡πÑ‡∏°‡πà‡∏ä‡∏≠‡∏ö
        food_ids.append(food.id)

    if len(food_data) < 5:  # ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ô‡πâ‡∏≠‡∏¢‡πÄ‡∏Å‡∏¥‡∏ô‡πÑ‡∏õ
        messages.warning(request, "‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏µ‡πà‡πÄ‡∏Ñ‡∏¢‡πÉ‡∏´‡πâ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏°‡∏µ‡∏ô‡πâ‡∏≠‡∏¢‡πÄ‡∏Å‡∏¥‡∏ô‡πÑ‡∏õ ‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥‡πÉ‡∏´‡πâ‡πÉ‡∏´‡πâ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏≠‡∏≤‡∏´‡∏≤‡∏£‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Å‡πà‡∏≠‡∏ô!")
        return render(request, 'core/recommend_food.html', {'recommendations': []})

    # üîπ 2. ‡πÉ‡∏ä‡πâ One-Hot Encoding ‡∏Å‡∏±‡∏ö‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà‡∏≠‡∏≤‡∏´‡∏≤‡∏£
    encoder = OneHotEncoder(handle_unknown='ignore')  # ‚úÖ ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ!
    category_array = np.array(list(category_list)).reshape(-1, 1)
    encoder.fit(category_array)  # Fit ‡∏î‡πâ‡∏ß‡∏¢‡∏Ñ‡πà‡∏≤‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà‡∏ó‡∏µ‡πà‡∏£‡∏π‡πâ‡∏à‡∏±‡∏Å

    transformed_data = []
    for food in food_data:
        price = food[0]
        category_vector = encoder.transform(np.array(food[1:]).reshape(-1, 1)).toarray().sum(axis=0)  # One-Hot
        transformed_data.append([price] + list(category_vector))

    # üîπ 3. ‡πÉ‡∏ä‡πâ MinMaxScaler ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡∏Ñ‡πà‡∏≤‡∏£‡∏≤‡∏Ñ‡∏≤‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡∏ä‡πà‡∏ß‡∏á 0-1
    scaler = MinMaxScaler()
    scaled_data = scaler.fit_transform(transformed_data)

    # üîπ 4. ‡πÄ‡∏ó‡∏£‡∏ô‡πÇ‡∏°‡πÄ‡∏î‡∏• RandomForest
    clf = RandomForestClassifier(n_estimators=100, random_state=42)
    clf.fit(scaled_data, food_labels)

    # üîπ 5. ‡∏Ñ‡∏±‡∏î‡∏Å‡∏£‡∏≠‡∏á‡∏≠‡∏≤‡∏´‡∏≤‡∏£‡∏ó‡∏µ‡πà‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡πÄ‡∏Ñ‡∏¢‡∏ñ‡∏π‡∏Å‡πÉ‡∏´‡πâ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô
    unseen_foods = foods.exclude(id__in=food_ids)
    if not unseen_foods.exists():
        messages.warning(request, "‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏≠‡∏≤‡∏´‡∏≤‡∏£‡∏ó‡∏µ‡πà‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡πÉ‡∏´‡πâ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô")
        return render(request, 'core/recommend_food.html', {'recommendations': []})

    unseen_data = []
    unseen_food_list = []

    for food in unseen_foods:
        category_names = [c.name for c in food.category.all()]
        category_vector = encoder.transform(np.array(category_names).reshape(-1, 1)).toarray().sum(axis=0)  # One-Hot
        unseen_data.append([food.price] + list(category_vector))
        unseen_food_list.append(food)

    scaled_unseen_data = scaler.transform(unseen_data)  # Normalize ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà

    # üîπ 6. ‡πÉ‡∏ä‡πâ‡πÇ‡∏°‡πÄ‡∏î‡∏•‡∏Ñ‡∏≤‡∏î‡πÄ‡∏î‡∏≤ ‡πÅ‡∏•‡∏∞‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏≠‡∏≤‡∏´‡∏≤‡∏£‡∏ó‡∏µ‡πà‡πÇ‡∏°‡πÄ‡∏î‡∏•‡∏Ñ‡∏¥‡∏î‡∏ß‡πà‡∏≤‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏ô‡πà‡∏≤‡∏à‡∏∞‡∏ä‡∏≠‡∏ö
    predictions = clf.predict_proba(scaled_unseen_data)[:, 1]  # ‡πÉ‡∏ä‡πâ‡∏Ñ‡πà‡∏≤‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ô‡πà‡∏≤‡∏à‡∏∞‡πÄ‡∏õ‡πá‡∏ô‡∏Ç‡∏≠‡∏á‡∏Å‡∏≤‡∏£ "‡∏ä‡∏≠‡∏ö"
    sorted_indices = np.argsort(predictions)[::-1]  # ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏à‡∏≤‡∏Å‡∏Ñ‡πà‡∏≤‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ô‡πà‡∏≤‡∏à‡∏∞‡πÄ‡∏õ‡πá‡∏ô‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î
    recommended_foods = [unseen_food_list[i] for i in sorted_indices[:5]]  # ‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥ 5 ‡∏≠‡∏±‡∏ô‡∏î‡∏±‡∏ö‡πÅ‡∏£‡∏Å

    return render(request, 'core/recommend_food.html', {
        'recommendations': recommended_foods
    })